        "South East": ["abia", "anambra", "ebonyi", "enugu", "imo"],
        "South South": ["akwa-ibom", "bayelsa", "cross-river", "delta", "edo", "rivers"],
        "South West": ["ekiti", "lagos", "ogun", "ondo", "osun", "oyo"],
